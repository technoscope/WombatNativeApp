package com.example.wombatapp.support;

public class peopemodel {
    String name;
    String type;
    String description;
    String chargespersession;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getChargespersession() {
        return chargespersession;
    }

    public void setChargespersession(String chargespersession) {
        this.chargespersession = chargespersession;
    }
}
